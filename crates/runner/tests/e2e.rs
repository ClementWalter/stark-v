//! End-to-end tests comparing interpreter output with native execution.
//!
//! These tests automatically discover all examples from guest-lib and:
//! 1. Build guest ELF binaries (once per test run)
//! 2. Run them through the interpreter
//! 3. Compare the raw postcard bytes with native execution
//!
//! To add a new test, simply create a new module in guest/guest-lib/src/programs/
//! with a `test_call()` function that returns a serializable result.

use guest_lib::get_test_bytes;
use prover::e2e::run_guest_raw;

/// Test a single example by comparing guest output bytes with native output bytes.
fn test_example(name: &str) {
    let guest_bytes = run_guest_raw(name);
    let native_bytes = get_test_bytes(name).unwrap_or_else(|| panic!("Unknown example: {name}"));

    assert_eq!(
        guest_bytes, native_bytes,
        "Output mismatch for example '{name}'\n  Guest bytes: {guest_bytes:?}\n  Native bytes: {native_bytes:?}",
    );
}

// Individual test functions are auto-generated by build.rs
include!(concat!(env!("OUT_DIR"), "/e2e_tests.rs"));
